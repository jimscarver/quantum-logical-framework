b = 1.0 / np.sqrt(2)
ellipse_x = a * np.cos(t_fine)
ellipse_y = b * np.sin(t_fine)
ax1.plot(ellipse_x, ellipse_y, '--', color='gray', label='Ideal ellipse (conic section)')
ax1.legend()

# Optional 3D Bloch sphere view (full Hilbert embedding)
ax3d = fig.add_subplot(1, 2, 2, projection='3d')
ax3d.plot(bloch_vectors[:,0], bloch_vectors[:,1], bloch_vectors[:,2], 'o-', color='blue')
ax3d.set_title('3D Bloch Sphere Path\n(Underlying Hilbert-space geometry)')
