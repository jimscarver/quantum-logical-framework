# This module demonstrates the user's statement in the context of our framework:
#   "2 dimensional quantum bit transitions can be mapped to discrete conic section polygon
#    in the Hilbert space defining a projected line in a hologram."
#
# EXACT MAPPING AND JUSTIFICATION (tied to Hilbert space, Pauli folding, zero free action, and holography):
#
# 1. 2D QUANTUM BIT = SINGLE QUBIT IN HILBERT SPACE ℂ²
#    The fundamental object is a qubit living in the 2-dimensional complex Hilbert space ℋ = ℂ².
#    Any transition between states is generated by a unitary fold U(θ) = cos(θ/2)I − i sin(θ/2)(n·σ).
#    This is exactly a Pauli-generated rotation (quaternion multiplication), preserving zero free action
#    (unitary ⇒ balanced distinctions, no net imbalance).
#
# 2. DISCRETE TRANSITIONS → DISCRETE POINTS IN HILBERT SPACE
#    We sample the transition at discrete steps Δθ (N steps). Each step is a small fold.
#    The resulting pure states |ψ_k⟩ (k = 0 … N) are points on the unit sphere in the real 4-dimensional
#    embedding of ℋ (Reα, Imα, Reβ, Imβ with |α|² + |β|² = 1).
#    These points form a discrete polygonal path on the 3-sphere S³ ⊂ ℝ⁴.
#
# 3. MAPPING TO DISCRETE CONIC SECTION POLYGON
#    We extract the Bloch vector r = (⟨σx⟩, ⟨σy⟩, ⟨σz⟩) for each |ψ_k⟩.
#    For a rotation about a tilted axis (not aligned with z), the path of r traces a great circle on the Bloch sphere S².
#    When projected onto the x–y plane (a 2D slice), a great circle appears as an ellipse — a classic conic section.
#    Connecting the discrete Bloch (x,y) points with straight lines yields a closed (or open) polygon that approximates this ellipse.
#    This polygon lives in the projected Hilbert-space coordinates and is therefore a "discrete conic section polygon".
#
# 4. DEFINING A PROJECTED LINE IN A HOLOGRAM
#    In our holographic picture (spin-1 emergent from spin-1/2 distinctions), the true microscopic information lives on a lower-dimensional boundary.
#    The bulk transition (the 2D conic polygon in Hilbert space) is holographically encoded on the boundary as a straight line segment.
#    Mathematically: we project the polygon points onto a single linear observable (here the cumulative phase φ_k or the parameter t itself).
#    Because the underlying distinctions are relational and the bulk geometry is emergent, the information collapses to a 1D straight line on the holographic screen.
#    This is the exact analogue of how a bulk geodesic in AdS projects to a straight interval on the CFT boundary.
#    Energy relation reminder: each new discrete fold adds effective dimensionality (or resolves a new distinction), so E scales with the number of steps × h × f.
#
# 5. UNIVERSALITY AND CONSISTENCY WITH EARLIER MODULES
#    The same primitive folds used for frequency doubling, CNOT, etc., generate this transition.
#    The polygon + holographic line construction is built purely from zero-action distinctions and non-commutative folding; no extra ontology required.
#
# =============================================================================

# Pauli matrices (as before)
sigma_x = np.array([[0, 1], [1, 0]], dtype=complex)
sigma_y = np.array([[0, -1j], [1j, 0]], dtype=complex)
sigma_z = np.array([[1, 0], [0, -1]], dtype=complex)
I = np.eye(2, dtype=complex)

def pauli_rotation(axis, theta_deg):
    """Single fold: SU(2) rotation = unit-quaternion multiplication."""
    theta = np.deg2rad(theta_deg)
    n = np.array(axis) / np.linalg.norm(axis)
    return (np.cos(theta/2) * I
            - 1j * np.sin(theta/2) * (n[0]*sigma_x + n[1]*sigma_y + n[2]*sigma_z))

# =============================================================================
# EXAMPLE: DISCRETE QUBIT TRANSITION (TILTED ROTATION)
# =============================================================================
N_steps = 12                              # discrete folds → 12-sided polygon
theta_total = 360.0                       # full 360° rotation (great circle)
axis = [1.0, 1.0, 0.0]                    # tilted axis → projected ellipse (conic)

initial_state = np.array([1.0, 0.0], dtype=complex)  # start at |0⟩

states = [initial_state]
bloch_vectors = []

for k in range(1, N_steps + 1):
    delta_theta = theta_total / N_steps
    U = pauli_rotation(axis, delta_theta)
    psi_new = U @ states[-1]
    states.append(psi_new)
    
    # Compute Bloch vector (projection of Hilbert-space point)
    rho = np.outer(psi_new, psi_new.conj())          # density matrix
    rx = np.real(np.trace(rho @ sigma_x))
    ry = np.real(np.trace(rho @ sigma_y))
    rz = np.real(np.trace(rho @ sigma_z))
    bloch_vectors.append([rx, ry, rz])

bloch_vectors = np.array(bloch_vectors)

print("=== EXAMPLE SETUP ===")
print(f"2D qubit transition: {N_steps} discrete Pauli folds along tilted axis {axis}")
print(f"Total rotation: {theta_total}° → great circle on Bloch sphere")
print("Projection to x–y plane yields ellipse (conic section)")

# =============================================================================
# PLOT 1: DISCRETE CONIC SECTION POLYGON IN PROJECTED HILBERT SPACE
# =============================================================================
fig = plt.figure(figsize=(14, 6))

ax1 = fig.add_subplot(1, 2, 1)
x = bloch_vectors[:, 0]
y = bloch_vectors[:, 1]
ax1.plot(x, y, 'o-', color='blue', linewidth=2, markersize=6, label='Discrete polygon')
ax1.set_title('Discrete Conic Section Polygon\n(Projection of Qubit Transition in Hilbert Space)')
ax1.set_xlabel('Bloch x')
ax1.set_ylabel('Bloch y')
ax1.axis('equal')
ax1.grid(True)
ax1.legend()

# Overlay the ideal ellipse for reference (theoretical conic)
t_fine = np.linspace(0, 2*np.pi, 200)
# Analytic ellipse parameters for this tilted rotation projection
